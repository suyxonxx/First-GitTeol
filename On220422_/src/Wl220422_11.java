
public class Wl220422_11 {
	
	public static void main(String[] args) {
//		문자열의 첫번째 위치는 0번.
		String str = "ABCDEFG";
		
//		1. 문자열 str의 2번 문자부터 맨 끝까지
		String str1 = str.substring(2);
		System.out.println(str1);
		
//		2. 문자열 str의 2번 ~
		String str2 = str.substring(2, 5);
		System.out.println(str2);
		
	}//end of main
	
}//end of class